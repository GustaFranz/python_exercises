# EXERCICIO 80 - Listar chaves e valores do dicionario (contexto educacional)
#
# ENUNCIADO
# Dado o dicionario abaixo:
# notas = {"Ana": 8.0, "Bruno": 6.5, "Carla": 9.0, "Diego": 7.0}
# Exiba uma lista com apenas os nomes dos alunos (chaves).
# Depois exiba uma lista com apenas as notas (valores).
#
# ORIENTACOES
## Use .keys() para obter as chaves do dicionario.
## Use .values() para obter os valores.
## Converta para list() antes de exibir se quiser ver os itens como lista.
#
# --- Implemente sua solucao abaixo ---
#
# =============================================================================
# # RESOLUCAO DO EXERCICIO
# =============================================================================

notas = {"Ana": 8.0, "Bruno": 6.5, "Carla": 9.0, "Diego": 7.0}

print("Lista de alunos (chaves):")
print(list(notas.keys()))    

print("Lista de notas (valores):")
print(list(notas.values()))



